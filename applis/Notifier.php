  <h1>Notifier</h1>

  <div>
    <p>
      <b>Outil de surveillance de serveurs de jeu.</b><br />
      Cette application permet de détecter et de notifier l'utilisateur quand un joueur rejoint un des serveurs surveillés.
      La notification s'effectue par une alerte sonore ou visuelle, via une icône dans la zone de notification.
    </p>

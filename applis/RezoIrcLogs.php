  <h1>RezoIrcLogs</h1>

  <div>
    <p>
      <b>Application web permettant de parcourir des logs IRC.</b><br />
      Cette application lit et affiche dans un format agréable les logs IRC, en prenant soin de les organiser de façon hiérarchique et chronologique.
      Elle permet également d'effectuer des recherches à l'intérieur de ces logs.
    </p>

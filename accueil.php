<h1>Accueil</h1>
<p>

<p>
  Supélec Rézo est une association loi 1901 administrant le réseau internet de la résidence du campus de Supélec de Gif-sur-Yvette. 
</p>

<h3>Objectifs et missions</h3>
<p>
  <ul>
    <li>Fournir aux adhérents de l'association une connexion internet</li>
    <li>Favoriser la communication entre étudiants sur le campus, ce qui passe par l'offre de nombreux services tels qu'un service mail, un hébergement de sites internets, de fichiers...</li>
    <li>Offrir une aide technique aux adhérents en ce qui concerne leurs problème d'ordre informatique</li>
    <li>Maintenir et renouveler le parc informatique de la résidence</li>
  </ul>
</p>

<h3>Membres</h3>
<p>
  Supélec Rézo est consitué d'une vingtaine d'élèves de Supélec, aussi connu sous le nom de Rézomen.<br />
  Cette équipe s'occupe, en plus de l'administration du réseau, de tenir des permanences, organisées deux fois par semaines le lundi midi et le mercredi soir, ainsi que de répondre quotidiennement aux demandes formulées par ses adhérents. <br />
  Pour plus de précisions, visitez la <a href="?p=3">page des membres.</a>
</p>

<h3>Services</h3>
<p>
  <table>
    <tr>
      <td>Service mail</td>
      <td>Chaque adhérent se voit fournir à son inscription une adresse mail, et la possibilité de s'inscrire aux listes de distribution administrées par Supélec Rézo</td>
    </tr>
      <td>Service d'hébergement de fichiers</td>
      <td>Nous fournissons aux associations et clubs du campus la possibilité d'héberger des fichiers.</td>
    </tr>
    <tr>
      <td>Service d'hébergement web</td>
      <td>Nous proposons aux adhérents un service d'hébergement de sites web</td>
    </tr>
    <tr>
      <td>Site communautaire</td>
      <td>Nous proposons un site communautaire, contenant notamment un trombinoscope des étudiants de Supélec, un descriptif des clubs et associations du campus, ainsi qu'une section d'aide à l'adhérent.</td>
    </tr>
    <tr>
      <td>TNT</td>
      <td>Il est mis à la disposition des élèves la possibilité de regarder la Télévision Numérique Terrestre en multicast à la résidence</td>
    </tr>
    <tr>
      <td>IRC</td>
      <td>Nous hébergeons et administrons un serveur IRC de RézoSup</td>
    </tr>
  </table>

<h3>Federez</h3>
<p>
  Supélec Rézo est membre de <a href="www.federez.net">FedeRez</a>, fédération d'associations regroupant des réseaux étudiants venant de la France entière, et visant à favoriser l'échange entre ce dernières, tant par l'organisation d'évènements regroupants ses membres que par les conseils techniques qu'elle apporte afin de soutenir le développement des projets des associations qu'elle rassemble. FedeRez organise chaque année les <a href="www.federez.net/journees/">journées FedeRez</a>, évènement durant lequel sont organisées des conférences sur le thème de l'informatique.
</p>

<h3>Renater</h3>

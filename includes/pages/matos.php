<h1 class="page-title">Matériel</h1>

<h2>Serveurs</h2>
<p>
  Supélec Rézo possède un parc de 17 serveurs. Ces serveurs sont traditionnellement nommés à partir de noms de personnages de Star Wars.<br />

  Ce parc se compose de machines montées à partir de pièces achetées dans le commerce pour la plupart, mais également d'anciennes machines de membres, ainsi que de serveurs donnés à l'association.
</p>

<h2>Matériel Réseau</h2>

<h4>Switchs</h4>
<p>
  Nous possédons 5 modèles de switchs formant l'architecture filaire de la résidence, tous mis en production lors du renouvellement de l'architecture physique en novembre 2009. <br />

  <br />
  Voici une liste des modèles que nous utilisons:
</p>

<ul>
    <li><b>Le master switch</b>: <em class='blue'>5412zl</em>, surnommé DeathStar. Il relie entre elles les résidences I. II. et III. en optiques.</li>
    <li><b>Les switchs 24 ports</b>: <em class='blue'>ProCurve 2910al-24G</em></li>
    <li><b>Les switchs 48 ports</b>: <em class='blue'>ProCurve 2910al-48G</em></li>
    <li><b>Les switchs serveurs</b>: <em class='blue'>Procurve 1700 24 ports</em></li>
    <li><b>Le switch en salle polyvalente</b>: <em class='blue'>Procurve 1810 8 ports</em></li>
</ul>

<h4>Points d'accès WiFi</h4>

<p>
  Les premiers points d'accès WiFi ont été installés à la résidence en 2008. Il s'agit de bornes <em class="blue">Cisco aironet 1130</em>.
</p>

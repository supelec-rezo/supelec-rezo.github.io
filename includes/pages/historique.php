<h1 class="page-title">Historique</h1>

<h3>2010</h3>
<p>
  Rénovation complète du local Rézo, au rez-de-chaussée la résidence II.C.
</p>

<h3>Fin 2009</h3>
<p>
  Supélec Rézo renouvelle entièrement son architecture physique, accomplissement d'un projet datant de 2007. De nombreux switchs sont achetés à HP avec l'aide du CRI de Supélec et de CESAL (Organisme gérant de la résidence des élèves de Supélec) ; ils seront installés entre novembre et décembre 2009.
</p>

<h3>2009</h3>
<p>
  Supélec Rezo fonde <a href="http://www.federez.net">FedeRez</a>, en association avec <a href="http://br.binets.fr/Accueil">Binet Réseau</a> (Polytechnique), <a href="http://www.crans.org/">CR@NS</a> (École Normale Supérieure de Cachan), <a href="http://resel.fr/">ResEl</a> (TELECOM Bretagne - Institut TELECOM), <a href="http://www.rezel.net/">Rezel</a> (TELECOM ParisTech - Télécom Paris - Institut TELECOM), <a href="http://www.rez-rennes.supelec.fr/">Supélec Rézo Rennes</a> (Supélec campus de Rennes) et <a href="http://www.via.ecp.fr/">VIA Centrale Réseaux</a> (École Centrale Paris ), la Fédération des Réseaux Étudiants.<br />
  <br />
  
  Ceci a pu enfin s'accomplir après de très longues discussions au cours des années précédentes, principalement sur IRC <a href="irc://irc.rezosup.org/#federez">irc.rezosup.org/#federez</a> et au cours des journées FedeRez. <br />
  Les status ont été adoptés après une réunion marathon durant les journées FedeRez de l'année 2009 organisée par l'École Normale Supérieure de Cachan. <br />
  
  <br />
  FedeRez est officiellement créée le 08 Août 2009 (WALDEC : W751201020).
</p>

<h3>2008</h3>
<p>
  Installation des premières bornes WiFi à la résidence.
</p>

<h3>26 mars 2008</h3>
<p>
  Création du Conseil d'<strong>Administration de Supélec Rézo</strong>. <br />
  Au fil des années le nombre de Rézomen a augmenté (pour atteindre un nombre record de 15 personnes impliquées dans la promotion 2010). Cela introduisit des problématiques d'organisation, que la réforme des statuts créant le Conseil d'Administration a tenté de résoudre.
</p>

<h3>Septembre 1996</h3>
<p>
  Fin des câblages de la résidence I, plus ancienne que la résidence II.
</p>

<h3>Septembre 1995</h3>
<p>
  Lors de sa <strong>mise en service</strong> à la rentrée de septembre 1995, le réseau interconnecte 80 ordinateurs en résidence II exclusivement. <br />
  Les Rézomen ont à l'époque en effet préféré se concentrer sur le déploiement dans cette nouvelle résidence ouverte en Septembre 1994.
</p>

<h3>1991 - 1994</h3>
<p>
  Il faudra attendre trois ans et demi pour que le réseau (en réalité, une partie de celui-ci) soit mis en service, suite à un travail considérable d'étude et de financement mené par les premiers Rezomen. <br />
  Les comptes rendus de réunion de ces temps "anciens" font état d'une collaboration inter-campus particulièrement active, les "Supélec Rézo" rennais et messin étant alors rattachés à celui de Gif.
</p>

<h3>Juin 1991</h3>
<p>
  <strong>Création de l'association</strong> le 11 Juin 1991, fondée par Alexandre LACHMANN, Corinne LAUER et Valérie ABRELL. <br />
  L'objet de l'association Supélec Rézo est alors de créer et gérer un "réseau voix-données-images" dans la résidence des élèves.
</p>